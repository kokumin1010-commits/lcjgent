// Helper function to get translation
export function getLiverTranslation(key: string, lang: LiverLanguage): string {
  const entry = liverTranslations[key];
  if (!entry) return key;
  // zh-TWにフォールバック: zh-TW → zh → ja
  if (lang === "zh-TW") {
    return entry["zh-TW"] || entry["zh"] || entry["ja"] || key;
  }
  return entry[lang] || entry["ja"] || key;
}

// React hook helper
export function createLiverT(lang: LiverLanguage) {
  return (key: string, params?: Record<string, string | number>): string => {
    let text = getLiverTranslation(key, lang);
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, String(v));
      });
    }
    return text;
  };
}
