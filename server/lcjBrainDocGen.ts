import PptxGenJS from "pptxgenjs";
import { Document, Packer, Paragraph, TextRun, HeadingLevel, AlignmentType, BorderStyle } from "docx";
import { storagePut } from "./storage";
import { invokeLLM } from "./_core/llm";

// ============================================================
// PPT Generation
// ============================================================

interface PptSlide {
  title: string;
  content: string[];  // bullet points or paragraphs
  notes?: string;
}

interface PptRequest {
  title: string;
  slides: PptSlide[];
  theme?: "dark" | "light" | "corporate";
}

/**
 * Generate a PPT file from structured slide data
 */
export async function generatePPT(request: PptRequest): Promise<{ url: string; fileName: string }> {
  const pptx = new PptxGenJS();
  
  // Set presentation properties
  pptx.author = "LCJ Brain";
  pptx.title = request.title;
  pptx.subject = request.title;
  
  // Theme colors
  const themes = {
    dark: { bg: "1a1a2e", title: "e94560", text: "ffffff", accent: "7c3aed" },
    light: { bg: "ffffff", title: "1e293b", text: "334155", accent: "6366f1" },
    corporate: { bg: "f8fafc", title: "0f172a", text: "1e293b", accent: "2563eb" },
  };
  const theme = themes[request.theme || "corporate"];
  
  // Title slide
  const titleSlide = pptx.addSlide();
  titleSlide.background = { color: theme.bg };
  titleSlide.addText(request.title, {
    x: 0.5, y: 1.5, w: 9, h: 2,
    fontSize: 36, bold: true, color: theme.title,
    align: "center", valign: "middle",
  });
  titleSlide.addText("Generated by LCJ Brain", {
    x: 0.5, y: 4.5, w: 9, h: 0.5,
    fontSize: 12, color: theme.accent,
    align: "center",
  });
  titleSlide.addText(new Date().toLocaleDateString("ja-JP"), {
    x: 0.5, y: 5.0, w: 9, h: 0.5,
    fontSize: 11, color: theme.text,
    align: "center",
  });
  
  // Content slides
  for (const slideData of request.slides) {
    const slide = pptx.addSlide();
    slide.background = { color: theme.bg };
    
    // Title
    slide.addText(slideData.title, {
      x: 0.5, y: 0.3, w: 9, h: 0.8,
      fontSize: 24, bold: true, color: theme.title,
      align: "left",
    });
    
    // Divider line
    slide.addShape("rect" as any, {
      x: 0.5, y: 1.1, w: 9, h: 0.03,
      fill: { color: theme.accent },
    });
    
    // Content bullets
    const bulletText = slideData.content.map(line => ({
      text: line,
      options: { fontSize: 16, color: theme.text, bullet: { type: "bullet" as const }, breakLine: true },
    }));
    
    slide.addText(bulletText as any, {
      x: 0.5, y: 1.4, w: 9, h: 4.5,
      fontSize: 16, color: theme.text,
      valign: "top",
      paraSpaceAfter: 8,
    });
    
    // Notes
    if (slideData.notes) {
      slide.addNotes(slideData.notes);
    }
  }
  
  // Generate buffer
  const buffer = await pptx.write({ outputType: "nodebuffer" }) as Buffer;
  
  // Upload to S3
  const fileName = `${request.title.replace(/[^\w\u4e00-\u9fff\u3040-\u309f\u30a0-\u30ff]/g, "_")}_${Date.now()}.pptx`;
  const key = `lcj-brain/documents/${fileName}`;
  const { url } = await storagePut(key, buffer, "application/vnd.openxmlformats-officedocument.presentationml.presentation");
  
  return { url, fileName };
}

// ============================================================
// Word Document Generation
// ============================================================

interface DocSection {
  heading?: string;
  headingLevel?: 1 | 2 | 3;
  paragraphs: string[];
}

interface DocRequest {
  title: string;
  sections: DocSection[];
}

/**
 * Generate a Word document from structured content
 */
export async function generateWord(request: DocRequest): Promise<{ url: string; fileName: string }> {
  const children: any[] = [];
  
  // Title
  children.push(
    new Paragraph({
      text: request.title,
      heading: HeadingLevel.TITLE,
      alignment: AlignmentType.CENTER,
      spacing: { after: 400 },
    })
  );
  
  // Date
  children.push(
    new Paragraph({
      children: [
        new TextRun({
          text: `生成日: ${new Date().toLocaleDateString("ja-JP")}`,
          size: 20,
          color: "666666",
        }),
      ],
      alignment: AlignmentType.CENTER,
      spacing: { after: 600 },
    })
  );
  
  // Sections
  for (const section of request.sections) {
    if (section.heading) {
      const level = section.headingLevel === 1 ? HeadingLevel.HEADING_1
        : section.headingLevel === 3 ? HeadingLevel.HEADING_3
        : HeadingLevel.HEADING_2;
      children.push(
        new Paragraph({
          text: section.heading,
          heading: level,
          spacing: { before: 400, after: 200 },
        })
      );
    }
    
    for (const para of section.paragraphs) {
      children.push(
        new Paragraph({
          children: [new TextRun({ text: para, size: 22 })],
          spacing: { after: 200 },
        })
      );
    }
  }
  
  const doc = new Document({
    sections: [{
      properties: {},
      children,
    }],
  });
  
  const buffer = await Packer.toBuffer(doc);
  
  // Upload to S3
  const fileName = `${request.title.replace(/[^\w\u4e00-\u9fff\u3040-\u309f\u30a0-\u30ff]/g, "_")}_${Date.now()}.docx`;
  const key = `lcj-brain/documents/${fileName}`;
  const { url } = await storagePut(key, Buffer.from(buffer), "application/vnd.openxmlformats-officedocument.wordprocessingml.document");
  
  return { url, fileName };
}

