/**
 * Use AI to generate structured PPT content based on user request and context data
 */
export async function aiGeneratePptContent(userRequest: string, contextData: string): Promise<PptRequest> {
  const prompt = `你是一个专业的PPT内容策划师。根据用户的需求和提供的数据，生成一个结构化的PPT内容。

用户需求: ${userRequest}

可用数据:
${contextData.substring(0, 10000)}

请生成一个JSON格式的PPT结构，包含6-12页幻灯片。格式如下:
{
  "title": "PPT标题",
  "theme": "corporate",
  "slides": [
    {
      "title": "幻灯片标题",
      "content": ["要点1", "要点2", "要点3"],
      "notes": "演讲备注（可选）"
    }
  ]
}

要求:
1. 内容专业、有条理
2. 每页3-6个要点
3. 数据引用必须来自提供的数据
4. 如果没有具体数据，用合理的框架和建议填充
5. theme可选: "dark"(深色科技风), "light"(浅色简约), "corporate"(商务蓝)
6. 只输出JSON，不要其他文字`;

  const result = await invokeLLM({
    model: "gpt-4.1-mini",
    messages: [
      { role: "system", content: "你是PPT内容生成专家。只输出有效的JSON格式。" },
      { role: "user", content: prompt },
    ],
    response_format: { type: "json_object" },
  });

  const content = result.choices?.[0]?.message?.content || "{}";
  try {
    const parsed = JSON.parse(content);
    return {
      title: parsed.title || userRequest,
      theme: parsed.theme || "corporate",
      slides: parsed.slides || [{ title: "内容", content: ["暂无内容"] }],
    };
  } catch {
    return {
      title: userRequest,
      theme: "corporate",
      slides: [{ title: "内容生成失败", content: ["请重试"] }],
    };
  }
}

